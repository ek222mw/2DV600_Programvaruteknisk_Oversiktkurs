package Exercise1;

public class CollectMain{
	
	public static void main(String[] args)
	{
		//List
		ArrayIntList it = new ArrayIntList();
		ArrayIntStack is = new ArrayIntStack();
		//adds.
		it.add(1);
		it.add(2);
		it.add(3);
		it.add(4);
		it.add(10);
		it.add(6);
		it.add(7);
		it.add(8);
		
		//removes
		//it.remove(3);
		//adds integer at given position.
		it.addAt(5, 1);
		//prints index of given value.
		System.out.println(it.indexOf(1));
		
		//Stack
		//adds to the stack
		is.push(1);
		is.push(2);
		is.push(3);
		is.push(4);
		is.push(5);
		
		//removes last added integer and returns the value of it.
		System.out.println(is.pop());
		//peek at last added integer.
		System.out.println(is.peek());
	}

}
